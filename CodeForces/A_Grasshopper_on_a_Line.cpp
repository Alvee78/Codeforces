#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t;
    cin>>t;
    while (t--)
    {
        ll n,k;
        cin>>n>>k;
        if(n%k==0){
            cout<<2<<endl<<n-1<<" "<<1<<endl;
        }else{
            cout<<1<<endl<<n<<endl;
        }
    }
    
    return 0;
}
