#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t,n,n1;
    cin>>t;
    string s ,s1 ;
    while (t--)
    {
        cin>>s>>s1;
        n=s.size();
        n1=s1.size();
        string r , r1;
        int lcm=(n*n1)/__gcd(n,n1);
        for(int i=0;i<lcm/n;i++)r+=s;
        for(int i=0;i<lcm/n1;i++)r1+=s1;
        if(r==r1)cout<<r<<endl;
        else cout<<-1<<endl;
    }
    
    return 0;
}
