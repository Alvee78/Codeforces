#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t,n,k=1,l=2;
    cin>>t;
    while (t--)
    {
        cin>>n;
        k=1,l=2;
        if(n==2){
            cout<<-1<<endl;
        }else{
            for(int i=0;i<n;i++){
                for(int j=0;j<n;j++){
                    if(k>n*n){
                        cout<<l<<" ";
                        l+=2;
                    }else{
                        cout<<k<<" ";
                        k+=2;
                    }
                }
                cout<<endl;
            }
        }
    }
    
    return 0;
}
