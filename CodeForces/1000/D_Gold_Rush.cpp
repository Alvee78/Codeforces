#include<bits/stdc++.h>
using namespace std;
#define ll long long

int solve(ll n,ll m){
    if(n==m){
        return 1;
    }
    else if(n%3!=0){
        return 0;
    }
    if(((n/3)==m)||((2*(n/3))==m)){
        return 1;
    }else if(((n/3)>m)||((2*(n/3))>m)){
        return (solve((n/3),m)||(solve((2*(n/3)),m)));
    }else{
        return 0;
    }
}

int main(){
    int t;
    cin>>t;
    while (t--)
    {
        ll n,m;
        cin>>n>>m;
        solve(n,m)?cout<<"YES"<<endl:cout<<"NO"<<endl;
    }
    
    return 0;
}
