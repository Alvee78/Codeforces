using namespace std;
#define ll long long

int main(){
    ll n,m,s=-1,in,t=0;
    cin>>n>>m;
    for(int i=1;i<=n;i++){
        cin>>s;
        s=(s/m)+(s%m!=0);
        if(s>=t && s!=-73){
            in=i;
            t=s;
        }
    }
    if(t==0){
        cout<<n;
    }else{
        cout<<in;
    }
    return 0;
}
