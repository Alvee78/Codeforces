#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t;
    cin>>t;
    while (t--)
    {
        ll n,s;
        cin>>n>>s;
        
            if (n%2){
                cout<<s<<" ";
                for(int i=0+2;i<(n-1+2)/2;i++){
                    cout<<i<< " "<<-i<<" ";
                }cout<<endl;
            }else{
                cout<<s-1<<" "<<s+1<< " ";
                for(int i=0+2;i<(n)/2;i++){
                    cout<<i<< " "<<-i<<" ";
                }cout<<endl;
            }
        
    }
    
    return 0;
}
