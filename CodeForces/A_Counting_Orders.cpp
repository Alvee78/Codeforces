#include<bits/stdc++.h>
using namespace std;
#define ll long long
const ll mod (1e9+7);

int main(){
    ll t,n,j;
    cin>>t;

    while (t--)
    {
        cin>>n;
        ll a[n],b[n];
        vector<ll>res(n);
        for(int i=0;i<n;i++){
            cin>>a[i];
        }
        for(int i=0;i<n;i++){
            cin>>b[i];
        }
        sort(a,a+n);
        sort(b,b+n);
        int i=0;
        ll c=1;
        j=0;
        for(int i=0;i<n;i++){
            for(;a[j]<=b[i]&& j!=n;){
                j++;
            }
            if(n!=j)
            res[i]=n-j;
        }
        
        //reverse(res.begin(),res.end());
        j=res.size()-1;
        n=j;
        for( i=0;i<=n;i++,j--){
            c=((c%mod)*((res[j]-i)%mod))%mod;
        }
        cout<<c<<endl;
        
    }
    
    return 0;
}
