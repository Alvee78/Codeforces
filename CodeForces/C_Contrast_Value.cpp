#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t;
    cin>>t;
    while (t--)
    {
        int n,c=0;
        cin>>n;
        vector<ll>v(n);
        
        for(int i=0;i<n;i++){
            cin>>v[i];
        }
        if(n==1)cout<<1<<endl;
        else {
            int tr=0,tr2=0;
            for(int i=0;i<n-1;i++){
                if (v[i]>v[i+1]){
                    if(tr==0)
                    {   
                        c++;
                        
                        tr=1;
                        tr2=0;
                    }
                }else if(v[i]<v[i+1]){
                    if(tr2==0)
                    {   
                        c++;
                        
                        tr=0;
                        tr2=1;
                    }
                }
            }
            cout<<c+1<<endl;
        }
        
    }
    
    return 0;
}
