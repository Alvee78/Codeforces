#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t,n,k;
    cin>>t;
    while (t--)
    {
        cin>>n>>k;
        char a[n+1];
        cin>>a;
        int s[26]={0},c[26]={0};
        for(int i=0;i<n;i++){
            if(a[i]<=90){
                c[a[i]-'A']++;
            }else{
                s[a[i]-'a']++;
            }
        }
        int ans=0,sml=0;
        for(int i=0;i<26;i++){
            int t,t2;
            t=c[i],t2=s[i];
            ans+=min(t,t2);
            c[i]-=min(t,t2);
            s[i]-=min(t,t2);
            sml+=c[i]/2+s[i]/2;
        }
        cout<<ans+min(k,sml)<<endl;
    }
    
    return 0;
}
