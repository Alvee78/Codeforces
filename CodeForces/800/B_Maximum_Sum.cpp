#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t,n,k;
    cin>>t;
    while (t--)
    {
        ll s=0;
        cin>>n>>k;
        ll a[n];
        for(int i=0;i<n;i++){
            cin>>a[i];
            s+=a[i];
        }
        sort(a,a+n);
        int i=0,j=n-k;
        int a1[k],a2[k];
        for(int q=0;q<k;q++){
            a1[i]=a[2*i]+a[(2*i)+1];i++;
            a2[i]=a[j];j++;
        }
        i=0,j=0;
        for(int q=0;q<k;q++){
            if(a1[i]<a2[j]){
                s-=a[i];i++;
            }else{
                s-=a[j];j++;
            }
        }
        cout<<s<<endl;
    }
    
    return 0;
}
