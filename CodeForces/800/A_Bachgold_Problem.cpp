#define ll long long

int main(){
    ll n;
    cin>>n;
    if(n%2!=0){
        cout<<((n-3)/2)+1<<endl;
        cout<<3<<" ";
        n-=3;
    }else{
        cout<<n/2<<endl;
    }
    for(int i=0;n!=0;i++){
        cout<<2<<" ";
        n-=2;
    }
    return 0;
}
