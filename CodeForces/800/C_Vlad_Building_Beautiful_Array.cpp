#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t;
    cin>>t;
    while (t--)
    {
        ll n,ch=0;
        cin>>n;
        ll a[n];
        for(int i=0;i<n;i++){
            cin>>a[i];
        }
        sort(a,a+n);
        if(a[0]%2){
            for(int i=1;i<n;i++){
                if(a[i]%2==0 and (a[i]-a[0])%2==0){
                    ch=1;
                }
            }
        }else{
            for(int i=1;i<n;i++){
                if(a[i]%2 and (a[i]-a[0])%2){
                    ch=1;
                }
            }
        }
        if(ch){
            cout<<"NO"<<endl;
        }else{
            cout<<"YES"<<endl;
        }
    }
    
    return 0;
}
