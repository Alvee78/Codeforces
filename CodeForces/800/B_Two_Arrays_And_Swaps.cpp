#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main()
{
    int t, n, k;
    cin >> t;
    while (t--)
    {
        cin >> n >> k;
        int a[n], b[n], s = 0;
        for (int i = 0; i < n; i++)
        {
            cin >> a[i];
            s += a[i];
        }
        for (int i = 0; i < n; i++)
        {
            cin >> b[i];
        }
        sort(a, a + n);
        sort(b, b + n);
        for (int i = 0; i < k; i++)
        {
            if (a[i] < b[n - 1 - i])
            {
                s -= a[i];
                s += b[n - 1 - i];
            }
            else
            {
                break;
            }
        }
        cout << s << endl;
    }

    return 0;
}
