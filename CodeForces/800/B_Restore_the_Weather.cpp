#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t;
    cin>>t;
    while (t--)
    {
        ll n,k,s;
        vector<pair<ll,ll>>vec;
        cin>>n>>k;
        for(int i=0;i<n;i++){
            cin>>s;
            vec.push_back({s,i});
        }
        vector<ll>vec2;
        for(int i=0;i<n;i++){
            cin>>s;
            vec2.push_back(s);
        }
        sort(vec.begin(),vec.end());
        sort(vec2.begin(),vec2.end());
        ll a[n];
        for(int i=0;i<n;i++){
            a[vec[i].second]=vec2[i];
        }
        for(int i=0;i<n;i++){
            cout<<a[i]<<" ";
        }
        cout<<endl;
    }
    
    return 0;
}
