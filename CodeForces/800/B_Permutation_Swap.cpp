#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        ll n;
        cin >> n;
        vector<int> v(n);
        map<ll, ll> map1, map2, map3;
        for (int i = 0; i < n; i++)
        {
            cin >> v[i];
            map1[v[i]] = i + 1;
        }
        for (int i = 0; i < n; i++)
        {
            map2[i + 1] = i + 1;
        }
        
        for (int i = 0; i < n; i++)
        {
            
            map3[abs( map1[i + 1] - map2[i + 1])]++;
        }
        ll gcd;
        for (auto x : map3)
        {
            
            gcd = x.first;
            break;
        }
        for (auto x : map3)
            gcd = __gcd(gcd, x.first);
        cout  <<  gcd  <<  endl;
    }
    return 0;
}
