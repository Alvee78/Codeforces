#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t,n;
    cin>>t;
    while (t--)
    {
        cin>>n;
        ll a[n],b[n];
        for(int i=0;i<n;i++){
            cin>>a[i];
        }
        for(int i=0;i<n;i++){
            cin>>b[i];
        }
        sort(a,a+n);
        sort(b,b+n);
        int j=0,l=1;
        for(int i=0;i<n;i++){
            for(;j<n;j++){
                if(b[j]>a[i]){
                    break;
                }
            }
            l=l*(j+1);
        }
        
        cout<<l<<endl;
    }
    
    return 0;
}
