using namespace std;
#define ll long long

int main(){
    int t,n;
    cin>>t;
    while (t--)
    {
        
        cin>>n;vector<int>v(n+1);
        for(int i=1;i<=n;i++){
            cin>>v[i];
        }
        int ans=0;
        for(int i=1;i<=n;i++){
            ans=__gcd(ans,abs(v[i]-i));
        }
        cout<<ans<<endl;
    }
    
    return 0;
}
