#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int t;
    cin>>t;
    while (t--)
    {
        int n,m=0,f=0,s=0;
        char a;
        cin>>n;
        for(int i=0;i<n;i++){
            cin>>a;
            if(a=='<'){
                f++;
                if (f>s)m++;
            }
            else{
                s++;
                if(s>f)m++;
            }
        }
        cout<<m+1<<endl;
    }
    
    return 0;
}
