#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    int n;
    cin>>n;
    for(int i=0;i<n;i++){
        double a,b,c,d,s;
        cin>>a>>b>>c>>d;
        double h;
        h =abs(a-c);
        s=b+d+h;
        s/=2;
        double area=sqrt(s*(s-b)*(s-d)*(s-h));
        h=2*area*(1/h);
        cout<<"Case "<<i+1<<": "<<setprecision(20)<<.5*(a+c)*h<<endl;
    }
    return 0;
}
