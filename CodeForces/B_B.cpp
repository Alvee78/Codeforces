#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    ll t,n,x,y;
    cin>>t;
    while (t--)
    {
        cin>>n;
        x=0,y=0;
        ll a[n];
        for(int i=0;i<n;i++){
            cin>>a[i];
            if(i%2==0){
                x=__gcd(x,a[i]);
            }else{
                y=__gcd(y,a[i]);
            }
        }
        ll tr=1,tr2=1;
        for(int i=0;i<n;i++){
            if(i%2==0 && a[i]%y==0){
                tr=0;
            }else if(i%2!=0 && a[i]%x==0){
                tr2=0;
            }
        }
        if (tr2){
            cout<<x<<endl;
        }else if(tr){
            cout<<y<<endl;
        }
            else{
            cout<<0<<endl;
        }
    }
    
    return 0;
}
