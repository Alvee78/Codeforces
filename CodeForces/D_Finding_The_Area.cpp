#include<bits/stdc++.h>
using namespace std;
#define ll long long

struct area
{
    ll a;
    ll b;
};


int main(){
    ll n;
    cin>>n;
    ll l=n;
    struct area a[n];
    for(int i=0;i<n;i++){
        cin>>a[i].a>>a[i].b;
    }
    ll ar=0;
    for(int i=0;i<n-1;i++){
        ar+=a[i].a*a[i+1].b;
        ar-=a[i].b*a[i+1].a;
    }
    ar+=a[n-1].a*a[0].b;
    ar-=a[n-1].b*a[0].a;
    cout<<abs(ar);
    
    return 0;
}
