#include <bits/stdc++.h>
using namespace std;
#define ll long long

int main()
{
    ll n;
    cin >> n;

    if (n < 10)
    {
        cout << n;
    }
    else if (n < 100)
    {
        cout << 9 + (n - 9) * 2;
    }
    else if (n < 1000)
    {
        cout << 9 + 90 * 2 + (n - 99) * 3;
    }
    else if (n < 10000)
    {
        cout << 9 + 90 * 2 + 900 * 3 + (n - 999) * 4;
    }
    else if (n < 100000)
    {
        cout << 9 + 90 * 2 + 900 * 3 + 9000 * 4 + (n - 9999) * 5;
    }
    else if (n < 1000000)
    {
        cout << 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + (n - 99999) * 6;
    }
    else if (n < 10000000)
    {
        cout << 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + (n - 999999) * 7;
    }
    else if (n < 100000000)
    {
        cout << 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + 9000000 * 7 + (n - 9999999) * 8;
    }
    else if (n < 1000000000)
    {
        cout << 9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + 9000000 * 7 + 90000000 * 8 + (n - 99999999) * 9;
    }
    else
    {
        cout << 8888888899;
    }

    return 0;
}